// PDF 1: Regular Checkup Report (Nurse)
// ═══════════════════════════════════════════════
export function generateCheckupPDF(data) {
  const doc = new jsPDF();
  let y = addHeader(doc, 'Regular Checkup Report');

  // Patient Info Section
  y = addSectionTitle(doc, 'Patient Information', y);
  y = addInfoRow(doc, 'Patient Name', data.patientName, y);
  y = addInfoRow(doc, 'Patient ID', data.patientId, y);
  y = addInfoRow(doc, 'Age / Gender', `${data.age || '—'} / ${data.gender || '—'}`, y);
  y = addInfoRow(doc, 'Date & Time', data.dateTime || new Date().toLocaleString('en-IN'), y);
  y = addInfoRow(doc, 'Nurse', data.nurseName || 'Duty Nurse', y);

  y += 5;

  // Vitals Table
  y = addSectionTitle(doc, 'Vital Signs', y);

  doc.autoTable({
    startY: y,
    head: [['Parameter', 'Value', 'Normal Range', 'Status']],
    body: [
      ['Blood Pressure', `${data.bp || '—'} mmHg`, '120/80 mmHg', getBPStatus(data.bp)],
      ['Blood Sugar', `${data.sugar || '—'} mg/dL`, '70-100 mg/dL', getSugarStatus(data.sugar)],
      ['Pulse Rate', `${data.pulse || '—'} bpm`, '60-100 bpm', getPulseStatus(data.pulse)],
      ['Temperature', `${data.temperature || '—'} °F`, '97-99 °F', getTempStatus(data.temperature)],
      ['Weight', `${data.weight || '—'} kg`, '—', 'Recorded'],
    ],
    theme: 'grid',
    headStyles: {
      fillColor: BRAND.dark,
      textColor: [255, 255, 255],
      fontSize: 9,
      fontStyle: 'bold',
    },
    bodyStyles: {
      fontSize: 9,
      textColor: BRAND.text,
    },
    alternateRowStyles: {
      fillColor: BRAND.light,
    },
    margin: { left: 14, right: 14 },
  });

  y = doc.lastAutoTable.finalY + 12;

  // Nurse Notes
  if (data.notes) {
    y = addSectionTitle(doc, 'Nurse Observations', y);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(...BRAND.text);
    const lines = doc.splitTextToSize(data.notes, 180);
    doc.text(lines, 14, y);
  }

  addFooter(doc);

  doc.save(`AarogyKendra_Checkup_${data.patientName || 'Report'}_${Date.now()}.pdf`);
  return doc;
}

