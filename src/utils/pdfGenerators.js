// PDF 2: Diagnosis + Prescription Report (Doctor)
// ═══════════════════════════════════════════════
export function generateDiagnosisPDF(data) {
  const doc = new jsPDF();
  let y = addHeader(doc, 'Diagnosis & Prescription Report');

  // Patient Info
  y = addSectionTitle(doc, 'Patient Information', y);
  y = addInfoRow(doc, 'Patient Name', data.patientName, y);
  y = addInfoRow(doc, 'Patient ID', data.patientId, y);
  y = addInfoRow(doc, 'Age / Gender', `${data.age || '—'} / ${data.gender || '—'}`, y);
  y = addInfoRow(doc, 'Doctor', data.doctorName || 'Attending Physician', y);
  y = addInfoRow(doc, 'Date', data.date || new Date().toLocaleDateString('en-IN'), y);

  y += 5;

  // Diagnosis Section
  y = addSectionTitle(doc, 'Diagnosis', y);
  y = addInfoRow(doc, 'Condition', data.diagnosis, y);
  y = addInfoRow(doc, 'Severity', data.severity || 'Moderate', y);
  y = addInfoRow(doc, 'Status', data.status || 'Confirmed', y);

  if (data.symptoms) {
    y = addInfoRow(doc, 'Symptoms', data.symptoms, y);
  }

  y += 5;

  // Prescription Table
  if (data.medicines && data.medicines.length > 0) {
    y = addSectionTitle(doc, 'Prescription', y);

    doc.autoTable({
      startY: y,
      head: [['#', 'Medicine', 'Dosage', 'Frequency', 'Duration']],
      body: data.medicines.map((med, i) => [
        i + 1,
        med.name || med.medicine || '—',
        med.dosage || '—',
        med.frequency || '—',
        med.duration || '—',
      ]),
      theme: 'grid',
      headStyles: {
        fillColor: BRAND.dark,
        textColor: [255, 255, 255],
        fontSize: 9,
        fontStyle: 'bold',
      },
      bodyStyles: {
        fontSize: 9,
        textColor: BRAND.text,
      },
      alternateRowStyles: {
        fillColor: BRAND.light,
      },
      margin: { left: 14, right: 14 },
    });

    y = doc.lastAutoTable.finalY + 12;
  }

  // Advice & Follow-up
  if (data.advice) {
    y = addSectionTitle(doc, 'Medical Advice', y);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(...BRAND.text);
    const lines = doc.splitTextToSize(data.advice, 180);
    doc.text(lines, 14, y);
    y += lines.length * 5 + 8;
  }

  if (data.followUp) {
    y = addSectionTitle(doc, 'Follow-up', y);
    doc.setFontSize(9);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(...BRAND.text);
    doc.text(data.followUp, 14, y);
  }

  addFooter(doc);

  doc.save(`AarogyKendra_Diagnosis_${data.patientName || 'Report'}_${Date.now()}.pdf`);
  return doc;
}

// ── Status Helpers ────────────────────────────
function getBPStatus(bp) {
  if (!bp) return '—';
  const sys = parseInt(bp);
  if (sys > 140) return '⚠ High';
  if (sys < 90) return '⚠ Low';
  return '✓ Normal';
}

function getSugarStatus(sugar) {
  if (!sugar) return '—';
  const val = parseInt(sugar);
  if (val > 140) return '⚠ High';
  if (val < 70) return '⚠ Low';
  return '✓ Normal';
}

function getPulseStatus(pulse) {
  if (!pulse) return '—';
  const val = parseInt(pulse);
  if (val > 100) return '⚠ High';
  if (val < 60) return '⚠ Low';
  return '✓ Normal';
}

function getTempStatus(temp) {
  if (!temp) return '—';
  const val = parseFloat(temp);
  if (val > 99.5) return '⚠ Fever';
  if (val < 96.5) return '⚠ Low';
  return '✓ Normal';
}
