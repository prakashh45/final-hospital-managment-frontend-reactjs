import jsPDF from 'jspdf';
import 'jspdf-autotable';

// ── Brand Colors ──────────────────────────────
const BRAND = {
  cyan: [34, 211, 238],
  blue: [59, 130, 246],
  dark: [15, 23, 42],
  text: [30, 41, 59],
  muted: [100, 116, 139],
  light: [241, 245, 249],
};

/**
 * Add AarogyKendra branded header to PDF.
 */
function addHeader(doc, title) {
  // Header background
  doc.setFillColor(...BRAND.dark);
  doc.rect(0, 0, 210, 38, 'F');

  // Cyan accent line
  doc.setFillColor(...BRAND.cyan);
  doc.rect(0, 38, 210, 2, 'F');

  // Brand name
  doc.setFontSize(20);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(255, 255, 255);
  doc.text('AarogyKendra', 14, 18);

  // Subtitle
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(148, 163, 184);
  doc.text('AI-Powered Healthcare Management System', 14, 26);

  // Report title
  doc.setFontSize(8);
  doc.setTextColor(...BRAND.cyan);
  doc.text(title.toUpperCase(), 14, 33);

  // Date on right
  doc.setFontSize(8);
  doc.setTextColor(148, 163, 184);
  const date = new Date().toLocaleString('en-IN', {
    dateStyle: 'long',
    timeStyle: 'short',
  });
  doc.text(date, 196, 18, { align: 'right' });

  return 48; // Y offset after header
}

/**
 * Add branded footer to PDF.
 */
function addFooter(doc) {
  const pageHeight = doc.internal.pageSize.height;
  doc.setFontSize(7);
  doc.setTextColor(...BRAND.muted);
  doc.text(
    'Generated by AarogyKendra — AI-Powered Healthcare Management | Confidential Medical Record',
    105,
    pageHeight - 10,
    { align: 'center' }
  );
  doc.setDrawColor(...BRAND.cyan);
  doc.setLineWidth(0.5);
  doc.line(14, pageHeight - 16, 196, pageHeight - 16);
}

/**
 * Add a section title with accent line.
 */
function addSectionTitle(doc, title, y) {
  doc.setFontSize(11);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...BRAND.dark);
  doc.text(title, 14, y);
  doc.setDrawColor(...BRAND.cyan);
  doc.setLineWidth(0.8);
  doc.line(14, y + 2, 60, y + 2);
  return y + 10;
}

/**
 * Add key-value info rows.
 */
function addInfoRow(doc, label, value, y) {
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...BRAND.muted);
  doc.text(label + ':', 14, y);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(...BRAND.text);
  doc.text(String(value || '—'), 60, y);
  return y + 7;
}

